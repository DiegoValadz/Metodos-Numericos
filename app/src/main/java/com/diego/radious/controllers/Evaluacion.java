package com.diego.radious.controllers;

public class Evaluacion {
    private  double x;
    private  double ev;

    public Evaluacion(double x, double ev) {
        this.x = x;
        this.ev = ev;
    }

    public double getX() {
        return x;
    }

    public double getEv() {
        return ev;
    }
}
